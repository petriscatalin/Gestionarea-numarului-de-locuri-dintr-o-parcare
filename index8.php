<p class="impact">Odata cu cresterea numarului de achizitii de masini, si cu cresterea populatiei din mediul urban, problema numarului de locuri de parcare a devenit tot mai des intalnita, mai ales in orasele mari si in zolonele suprapopulate ale acestora. Specialistii afirma faputl ca in momentul de fata numarul automobileler depaseste peste 1,2 miliarde, iar pana in 2035 se va ajunge la pragul de doua miliarde, urmand ca in 2050 sa se ajunga la aproximativ 2,5 miliarde de automobile, ceea ce reprezinta un numar imens in comparatie cu faptul ca in momentul de fata populatia planentei este de aproximativ 7,5 miliarde.
	Potrivit unui studiu in anul 2018, la nivel mondial, au fost vandute circa 79 de milioane de autoturisme, cu 0,8% mai putin decat in 2017(tabelul 1). Cele mai multe automobile fiind produse in Asia(aproximativ 42 de milioane), principala tara producatoare fiind China, urmata de Europa(aproape 19 milioane).<br><br><img src="imagini/vehicles.jpg"><br><br>
    Cel mai aglomerat oras la nivel mondial, din pumctul de vedere al traficului, este Bangkok(Thailanda), urmat de orase precum Mexico City(Mexic), Istambul(Turcia), Bucuresti(Romania), etc.. In Bucuresti sunt 1,4 milioane autoturisme, orasul avand o populatie de 1,8 milioane de locuitori. 
	Avand in vedere cifrele prezentate anterior, numarul locurilor de parcare este necesar in numar tot mai mare de la o zi la alta, datorita cresterii numarului de autovehicule, de aceea tot mai des se incearca dezbterea si gasirea unei solutii cat mai convenabile.
    In unele orase puternic dezvoltate in care s-a investit sume considerabile in gestionarea si furnizarea unor numar cat mai mare de locuri de parcare, este prezent un succes in evitarea aglomeratiei, cat si evitarea neplacerilor aparute intre locuitorii oraselor, insa sunt si multe alte orase in care gestionarea parcarilor este aproape inexistenta sau implementata doar pentru anumite parti ale orasului.<br><br>
	Gestionarea gresita a parcarilor poate duce la situatii ineficiente precum:<br>
	1)Parcarea in locuri necoresunzatoare si neamenajate in acest scop, de exemplu:pe trotuare, spatii verzi sau zone private;<br>
	2)Obstructionarea accesului pentru diverse vehicule pentru situatii de urgenta, de exemplu: salvare, pompieri sau politie;<br>
	3)Obstructionarea deplasarii pietonilor si biciclistilor, de exemplu: treceri de pietoni, piste pentru biciclisti;<br>
	4)Obstructionarea vizibilitatii celorlati participanti la trafic, de exemplu: intersectii.<br><br>
    Societatea umana este intr-o continua dezvoltare tehnologica, dar odata cu acest progres unul dintre principalele dezavantaje este poluarea, lucru care este luat destul deputin in considerare, precum si impactul poluarii asupra vietii oamenilor si a mediului inconjurator.
       Poluarea reprezinta contaminarea mediului inconjurator cu materiale care interfereaza cu sanatatea umana, calitatea vietii sau functia naturala a ecosistemelor(organismele vii si mediul in care traiesc). Poluarea aerului consta in schimbarea compozitiei sub aspectul proportiei dintre constituentii sai sau prin aparitia unor noi constituenti cu efecte daunatoare.
       Principalele surse de poluare ale aerului sunt:<br>
•	Arderea combustibililor fosili in producerea de energie electrica, transporturi, industrie si gospodarii;<br>
•	Procese industriale si utilizarea solventilor, de exemplu industria chimica si extractiva;<br>
•	Agricultura;<br>
•	Tratarea deseurilor;<br>
•	Eruptii vulcanice.<br>
Cea mai mare sursa de CO din poluarea generala a atmosferei(60%) este produsa de gazele de esapament a autovehiculelor. S-a estimat ca 80% din cantitatea de CO este produsa in primele doua minute de functionare a motorului. In comparatie cu masinile produse in urma cu 30 de ani, cele din ziua de azi polueaza mult mai putin, deoarece s-au intensificat preocuparile privind diminuarea poluarii si s-a optimizat procedeul de ardere si utilizarea dispozitivelor antipoluante.
Principalele particularitati ale amisiilor de poluanti ale autovehiculelor sunt ca eliminarea se face foarte aproape de sol, fapt care duce la realizarea unor concentratii ridicate la inaltimi foarte mici, si faptul ca emisiile se fac pe toate periada deplasarii.
Compozitia gazelor de esapament:<br>
•	CO-monoxid de carbon;<br>
•	CH-hidrocarburi;<br>
•	SO₂ -dioxid de sulf;<br>
•	NO-oxizi de azot;<br>
•	Compusi organici;<br>
•	Funingine.<br>

