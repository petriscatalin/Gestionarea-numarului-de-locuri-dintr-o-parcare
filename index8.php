Efectele pe care le are poluarea aerului asupra sanatatii omului sunt date de modificarile care apar in starea de sanatate a populatiei in urma expunerii acestora la agentii poluanti. Efectele de durata sunt reprezentate prin aparitia unor fenomene patologice, acestea fiind rezoltatul acumularii poluantilor in organism. Un efect foarte des intalnit este faptul ca sustemul respirator este afectat, omul avnd dificultati in respiratie. In zonele urbane, dens populate de autovehicule, au fost identificate in aer substante cert carcinogene, zone in care s-a identificat o crestere a frecventei cancerului. Cei mai raspanditi poluanti cancerigeni fiind hidrocarburile.<br>
Pe fondul degradarii tot mai accentuate a calitatii aerului, una dintre solutiile din ziua de azi sunt masinile electrice. Aceste masini utilizeaza energia electrica stocata intr-un acumulator folosit pentru alimentarea unui motor electric si pentru rotirea rotilor. Cand acesti acumulatori sunt epuizati, bateriile sunt reincarcate folosind curent de la reteaua electrica, fie de la o priza sau de la o unitate de incarcare dedicata. Emisiile provenite de la vehiculele electrice depind de regiunea geografica in care vehiculul este incarcat, atunci cand sunt conduse nu produc noxe neavand nici macar toba de esapament. Cu toate acestea, energia electrica pe care o utilizeaza poate produce diferite gaze, iar alte surse de poluare pot fi gasite la sursa generarii curentului, de exemplu, cea de extractie a combustibililor fosili. Cantitatea de poluare produsa depinde de modul in care este produsa energia electrica.



